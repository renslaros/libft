#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int int_min;

	int_min = -2147483648;
	if (n == int_min)
		ft_putstr_fd("-2147483648", fd);
	if (n < 0 && n != int_min)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	else if (n != int_min)
	{
		ft_putchar_fd((n + 48), fd);
	}
}
